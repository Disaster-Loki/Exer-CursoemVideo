meter = float(input("Distance in meters: "))
print("\tCorrespondence")

print("Kilometer: {}km".format(meter*0.001))
print("Hectometer: {}hm".format(meter*0.01))
print("Decameter: {}dam".format(meter*0.1))
print("Decimeter: {}dm".format(meter*10))
print("Centimeter: {}cm".format(meter*100))
print("Millimeter: {}mm".format(meter*1000))
